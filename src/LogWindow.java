public void tidy()
{
            for (int x = 0; x < 2; x++) {
            String s = jTextArea1.getText();
            jTextArea1.setText("");
            s = s.replaceAll("\n\n", "\n").replaceAll("\n \n", "\n");
            String[] ss = s.split("\n");
            for (int i = 0; i < ss.length; i++) {
                if (ss[i].length() == 21) {
                    ss[i] = "";
                }
            }
            for (int i = 0; i < ss.length; i++) {
                recieve(ss[i]);
            }
        }
}
