    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().equalsIgnoreCase("") || jTextField1.getText().equalsIgnoreCase(" ") || jTextField1.getText().equalsIgnoreCase("  ")) {
            JOptionPane.showMessageDialog(this, "Empty Field");
        } else {
            jTextArea1.getHighlighter().removeAllHighlights();
            Highlighter.HighlightPainter painter
                    = new DefaultHighlighter.DefaultHighlightPainter(Color.cyan);

            int offset = jTextArea1.getText().indexOf(jTextField1.getText());
            int length = jTextField1.getText().length();

            while (offset != -1) {
                try {
                    jTextArea1.getHighlighter().addHighlight(offset, offset + length, painter);
                    offset = jTextArea1.getText().indexOf(jTextField1.getText(), offset + 1);
                } catch (BadLocationException ble) {
                    System.out.println(ble);
                }
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

